CREATE TABLE BankTrans
       (TransID NUMBER(4) PRIMARY KEY,
	AcctNo NUMBER(4),
	OldBal NUMBER(7, 2),
	TransType VARCHAR2(1),
	TransAmt NUMBER(7,2),
	NewBal NUMBER(7,2),
	TransStat VARCHAR2(7));

CREATE TABLE ValidTrans
       (TransID NUMBER(4) PRIMARY KEY,
	TransType VARCHAR2(1),
	TransAmt NUMBER(7,2),
	Validity VARCHAR2(7));

CREATE TABLE InValidTrans
       (TransID NUMBER(4) PRIMARY KEY,
	TransType VARCHAR2(1),
	TransAmt NUMBER(7,2),
	Validity VARCHAR2(7));

